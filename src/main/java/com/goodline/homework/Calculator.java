package com.goodline.homework;

public class Calculator {
    public static int Sum(int a, int b) {
        return a + b;
    }
    public static int min(int a, int b) {
        return a - b;
    }
    public static int mult(int a, int b) {
        return a * b;
    }
    public static int div(int a, int b) {
        return a / b;
    }
}

